package br.com.ifpb.documentos;

public class MemorandoDePagamento {

	private String assunto, objetivodaDespesa, codigo, dataDeEmissao;

	public String getAssunto() {
		return assunto;
	}

	public void setAssunto(String assunto) {
		this.assunto = assunto;
	}

	public String getObjetivodaDespesa() {
		return objetivodaDespesa;
	}

	public void setObjetivodaDespesa(String objetivodaDespesa) {
		this.objetivodaDespesa = objetivodaDespesa;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getDataDeEmissao() {
		return dataDeEmissao;
	}

	public void setDataDeEmissao(String dataDeEmissao) {
		this.dataDeEmissao = dataDeEmissao;
	}

}
